// 간단한 키워드 매칭 기반 RAG 시뮬레이션 알고리즘
export function simulateRAGQuery(query) {
  if (!query || query.trim() === "") {
    return {
      answer: "질문을 입력해 주시면 라이즈/앵커 사업 지식베이스를 기반으로 답변해 드립니다.",
      sources: []
    };
  }

  const cleanQuery = query.toLowerCase();
  const scoredChunks = WIKI_CHUNKS.map(chunk => {
    let score = 0;
    // 1. 키워드 배열 일치도 계산 (가중치 3점)
    chunk.keywords.forEach(kw => {
      if (cleanQuery.includes(kw)) {
        score += 3;
      }
    });
    // 2. 제목 일치도 계산 (가중치 2점)
    if (chunk.title.toLowerCase().includes(cleanQuery) || cleanQuery.includes(chunk.unit.toLowerCase())) {
      score += 2;
    }
    // 3. 본문 텍스트 내 포함도 계산 (가중치 1점)
    const words = cleanQuery.split(/\s+/);
    words.forEach(word => {
      if (word.length >= 2 && chunk.content.toLowerCase().includes(word)) {
        score += 1;
      }
    });
    return { chunk, score };
  });

  // 점수가 높은 순으로 정렬하고 0점 초과인 것들만 수집
  const relevantResults = scoredChunks
    .filter(item => item.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 3); // Top 3 수집

  if (relevantResults.length === 0) {
    return {
      answer: `입력하신 질문("${query}")에 대해 지식베이스(ChromaDB 모의 색인) 내에서 직접 매칭되는 실적이나 지침을 찾을 수 없습니다. 라이즈 사업단 단위과제 기획서나 1차년도 성과보고서의 핵심 문서들을 바탕으로 다시 질문해 주세요.\n\n💡 **추천 질문**: "5극3특 공유대학이 뭐야?", "초광역 성장엔진 계획 알려줘", "회의비 식비 한도 얼마야?", "2차년도 예산 규모는?", "L-1 산식 알려줘"`,
      sources: []
    };
  }

  // 매칭된 조각들을 활용한 답변 생성기 (RAG 시뮬레이션)
  const topChunk = relevantResults[0].chunk;
  let answer = "";
  
  if (topChunk.category === "성과지표" && topChunk.unit === "지침") {
    answer = `RISE 사업단 공식 관리 지침에 근거한 **${topChunk.title}** 안내입니다:\n\n${topChunk.content}\n\n해당 기준은 대학 회계 원칙 및 국가연구개발혁신법 등 상위 법령 규정을 철저히 준수하여 집행되어야 합니다.`;
  } else if (topChunk.category === "사업비지침") {
    answer = `울산과학대 RISE 사업비 집행 규정에 의거한 **${topChunk.title}** 안내입니다:\n\n${topChunk.content}\n\n해당 비목 집행 시 증빙 서류 누락 및 한도 초과 집행은 불인정(환수) 사유이므로 상시 규정을 준수하여 주시기 바랍니다.`;
  } else if (topChunk.category === "사업비 현황") {
    answer = `RISE 사업단 2차년도 예산 재원에 근거한 **${topChunk.title}** 안내입니다:\n\n${topChunk.content}`;
  } else if (topChunk.category === "5극3특") {
    answer = `울산시 RISE 사업 광역 혁신 거버넌스 지침에 따른 **${topChunk.title}** 내용입니다:\n\n${topChunk.content}`;
  } else if (topChunk.category === "성과지표") {
    answer = `지자체 자율성과지표 및 추진 성과에 기반한 **${topChunk.title}** 상세 내역입니다:\n\n${topChunk.content}`;
  } else {
    answer = `라이즈(앵커) 사업 계획서에 기초한 **${topChunk.title}** 안내입니다:\n\n${topChunk.content}\n\n더 상세한 액션플랜이나 비목별 세부 예산 배정 현황은 해당 연도 탭 및 기획서 문서를 교차 점검해 보시길 권장합니다.`;
  }

  // 2등, 3등 조각이 있고 점수가 우수하다면 추가 참고 정보로 덧붙임
  if (relevantResults.length > 1 && relevantResults[1].score > 3) {
    answer += `\n\n📌 **연관 참고 지식 (${relevantResults[1].chunk.title})**:\n${relevantResults[1].chunk.content.substring(0, 150)}...`;
  }

  return {
    answer,
    sources: relevantResults.map(item => ({
      id: item.chunk.id,
      title: item.chunk.title,
      unit: item.chunk.unit,
      category: item.chunk.category
    }))
  };
}
