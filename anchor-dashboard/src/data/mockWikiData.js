// 간단한 키워드 매칭 기반 RAG 시뮬레이션 알고리즘
export function simulateRAGQuery(query, selectedYear = 2) {
  if (!query || query.trim() === "") {
    return {
      answer: "질문을 입력해 주시면 앵커 사업 지식베이스를 기반으로 답변해 드립니다.",
      sources: []
    };
  }

  const cleanQuery = query.toLowerCase();
  
  // 연차별 매칭을 위한 지문 보강
  const isQuerySpecifyingY1 = cleanQuery.includes("1차년도") || cleanQuery.includes("2025") || cleanQuery.includes("이전");
  const isQuerySpecifyingY2 = cleanQuery.includes("2차년도") || cleanQuery.includes("2026") || cleanQuery.includes("최신") || cleanQuery.includes("현재");
  
  const effectiveYear = isQuerySpecifyingY1 ? 1 : isQuerySpecifyingY2 ? 2 : selectedYear;

  const scoredChunks = WIKI_CHUNKS.map(chunk => {
    let score = 0;
    
    // 연차 일치 가중치 부여 (해당 연도 조각인 경우 가산점)
    if (chunk.year) {
      if (chunk.year === effectiveYear) {
        score += 4; // 연차 매칭 보너스 점수
      } else {
        score -= 5; // 다른 연도 조각인 경우 디버프 부여
      }
    }

    // 1. 키워드 배열 일치도 계산 (가중치 3점)
    chunk.keywords.forEach(kw => {
      if (cleanQuery.includes(kw)) {
        score += 3;
      }
    });
    // 2. 제목 일치도 계산 (가중치 2점)
    if (chunk.title.toLowerCase().includes(cleanQuery) || cleanQuery.includes(chunk.unit.toLowerCase())) {
      score += 2;
    }
    // 3. 본문 텍스트 내 포함도 계산 (가중치 1점)
    const words = cleanQuery.split(/\s+/);
    words.forEach(word => {
      if (word.length >= 2 && chunk.content.toLowerCase().includes(word)) {
        score += 1;
      }
    });
    return { chunk, score };
  });

  // 점수가 높은 순으로 정렬하고 0점 초과인 것들만 수집
  const relevantResults = scoredChunks
    .filter(item => item.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 3); // Top 3 수집

  if (relevantResults.length === 0) {
    return {
      answer: `입력하신 질문("${query}")에 대해 지식베이스(ChromaDB 모의 색인) 내에서 직접 매칭되는 실적이나 지침을 찾을 수 없습니다. 앵커 사업단 단위과제 기획서나 1차년도 성과보고서의 핵심 문서들을 바탕으로 다시 질문해 주세요.\n\n💡 **추천 질문**: "A1 전문기술인재 개요", "A1나 신산업특화센터에 대해 알려줘", "A2 창업 주관 부서가 어디야?", "5극3특 공유대학이 뭐야?", "회의비 식비 한도 얼마야?"`,
      sources: []
    };
  }

  // 매칭된 조각들을 활용한 답변 생성기 (RAG 시뮬레이션)
  const topChunk = relevantResults[0].chunk;
  let answer = "";
  
  if (topChunk.category === "성과지표" && topChunk.unit === "지침") {
    answer = `앵커 사업단 공식 관리 지침에 근거한 **${topChunk.title}** 안내입니다:\n\n${topChunk.content}\n\n해당 기준은 대학 회계 원칙 및 국가연구개발혁신법 등 상위 법령 규정을 철저히 준수하여 집행되어야 합니다.`;
  } else if (topChunk.category === "사업비지침") {
    answer = `울산과학대 앵커 사업비 집행 규정에 의거한 **${topChunk.title}** 안내입니다 (현재 검색 기준: ${effectiveYear === 1 ? "1차년도 지침" : "2차년도 최신 개정 지침"}):\n\n${topChunk.content}`;
    
    // 시기를 특정하지 않았거나 최신 버전 지침을 띄웠을 때 차이점을 친절히 추가 제공!
    if (topChunk.id === "rule-meeting" && !isQuerySpecifyingY1) {
      answer += `\n\n💡 **[시기별 주요 차이점 대조]**:\n- 1차년도(2026.04.01 이전) 지침: 1식 20,000원 한도\n- 2차년도(2026.07.01 최신) 지침: 1식 30,000원 한도 (e나라도움 전용 카드 필수 사용)`;
    } else if (topChunk.id === "rule-instructor" && !isQuerySpecifyingY1) {
      answer += `\n\n💡 **[시기별 주요 차이점 대조]**:\n- 1차년도(2026.04.01 이전) 지침: 최초 1시간 200,000원\n- 2차년도(2026.07.01 최신) 지침: 최초 1시간 300,000원 (초과 1시간당 20만원 가산)`;
    }
  } else if (topChunk.category === "사업비 현황") {
    answer = `앵커 사업단 ${effectiveYear}차년도 예산 재원에 근거한 **${topChunk.title}** 안내입니다:\n\n${topChunk.content}`;
  } else if (topChunk.category === "5극3특") {
    answer = `울산시 앵커 사업 광역 혁신 거버넌스 지침에 따른 **${topChunk.title}** 내용입니다:\n\n${topChunk.content}`;
  } else if (topChunk.category === "성과지표") {
    answer = `지자체 자율성과지표 및 추진 성과에 기반한 **${topChunk.title}** 상세 내역입니다:\n\n${topChunk.content}`;
  } else {
    answer = `앵커 사업 ${effectiveYear}차년도 계획서에 기초한 **${topChunk.title}** 안내입니다:\n\n${topChunk.content}\n\n더 상세한 액션플랜이나 비목별 세부 예산 배정 현황은 해당 연도 탭 및 기획서 문서를 교차 점검해 보시길 권장합니다.`;
  }

  // 2등, 3등 조각이 있고 점수가 우수하다면 추가 참고 정보로 덧붙임
  if (relevantResults.length > 1 && relevantResults[1].score > 3) {
    answer += `\n\n📌 **연관 참고 지식 (${relevantResults[1].chunk.title})**:\n${relevantResults[1].chunk.content.substring(0, 150)}...`;
  }

  return {
    answer,
    sources: relevantResults.map(item => ({
      id: item.chunk.id,
      title: item.chunk.title,
      unit: item.chunk.unit,
      category: item.chunk.category
    }))
  };
}
