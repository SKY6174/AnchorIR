-- 1. 위원회 마스터 테이블 생성
CREATE TABLE IF NOT EXISTS committees (
    id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    full_name TEXT,
    badge TEXT NOT NULL,
    color TEXT NOT NULL,
    purpose TEXT,
    description TEXT,
    constitution TEXT,
    cycle TEXT,
    functions TEXT[] NOT NULL DEFAULT '{}'::TEXT[],
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- 2. 위원회 소속 위원 명단 테이블 생성
CREATE TABLE IF NOT EXISTS committee_members (
    id BIGSERIAL PRIMARY KEY,
    committee_id TEXT NOT NULL REFERENCES committees(id) ON DELETE CASCADE,
    type TEXT NOT NULL, -- 위원장, 위원, 간사, 위원(자문겸직) 등
    name TEXT NOT NULL,
    org TEXT,           -- 소속 기관명 (예: 울산과학대학교, HD한국조선해양 등)
    dept TEXT,          -- 부서/학과명 (예: 기획처, 화학공학과 등)
    rank TEXT,          -- 직급/직위 (예: 총장, 처장, 교수, 부장 등)
    location TEXT,      -- 교내/교외 구분
    note TEXT,          -- 비고 (예: 신규 추가 등)
    sort_order INTEGER NOT NULL DEFAULT 0, -- 화면 정렬을 위한 순서 번호
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc'::text, NOW()) NOT NULL
);

-- 3. 개발자 편의성을 위해 로컬 및 초기 수준에서 RLS(행 단위 보안)를 비활성화합니다.
ALTER TABLE committees DISABLE ROW LEVEL SECURITY;
ALTER TABLE committee_members DISABLE ROW LEVEL SECURITY;

