CREATE POLICY rise_users_select_self
  ON public.rise_users
  FOR SELECT TO authenticated
  USING (uuid = auth.uid());

CREATE POLICY rise_users_admin_select
  ON public.rise_users
  FOR SELECT TO authenticated
  USING (public.is_rise_users_admin());

CREATE POLICY rise_users_admin_insert
  ON public.rise_users
  FOR INSERT TO authenticated
  WITH CHECK (public.is_rise_users_admin());

CREATE POLICY rise_users_admin_update
  ON public.rise_users
  FOR UPDATE TO authenticated
  USING (public.is_rise_users_admin())
  WITH CHECK (public.is_rise_users_admin());

CREATE POLICY rise_users_admin_delete
  ON public.rise_users
  FOR DELETE TO authenticated
  USING (public.is_rise_users_admin());

